// What is a Number?

// A number is a data type used to represent numeric values.
//  In programming, numbers can be categorized into different types,
//   such as integers (whole numbers) and floating-point numbers (decimal numbers).

// Example
var age = 21; // Integer
console.log(age);
var price = 99.9; // Floating Value
console.log(price);

// Basic Arithmetic Operations
// You can perform various arithmetic operations on number variables:

// Addition: +
// Subtraction: -
// Multiplication: *
// Division: /
// Modulus (Remainder): %

// exapmle
var a = 10;
var b = 2;
var sum = a + b;
console.log(sum);
var difference = a - b;
console.log(difference);
var product = a * b;
console.log(product);
var quotient = a / b;
console.log(quotient);
var remainder = a % b;
console.log(remainder);

// exapmple
var invalidNumber = 0 / 0;
console.log(invalidNumber); // NaN Not a Number

