// Unfamiliar operators

// Increment Operator (++)

// post increment ++
// ++pre increment

var x = 5;
var a = x++;
console.log(a);

var y = 10;
var b = ++y;
console.log(b);

// Decrement Operator (--)
// post increment --
// -- pre increment

var z = 5;
var c = z--;
console.log(c);
var d = --z;
console.log(d);

// Example
var num = 10;
var result = num++ + 5;
//   10 + 5
